package object;

import main.GamePanel;

import java.awt.*;
import java.awt.image.BufferedImage;

public class SuperObject {

    public BufferedImage image;
    public String name;
    public boolean collision = false;
    public int worldX, worldY;
    public Rectangle solidArea = new Rectangle(0, 0, 48, 48);   // BY DEFAULT
    public int solidAreaDefaultX = 0;
    public int solidAreaDefaultY = 0;

    // Draws the object unto the screen
    public void draw(Graphics2D g_2d, GamePanel gp) {

        int screenX = worldX - gp.player.worldX + gp.player.screenX;
        int screenY = worldY - gp.player.worldY + gp.player.screenY;

        //Only renders the map section where the camera is showing, which improves render performance
        if (worldX + gp.tileSize > gp.player.worldX - gp.player.screenX &&
                worldX - gp.tileSize < gp.player.worldX + gp.player.screenX &&
                worldY + gp.tileSize > gp.player.worldY - gp.player.screenY &&
                worldY - gp.tileSize < gp.player.worldY + gp.player.screenY) {

            g_2d.drawImage(image, screenX, screenY, gp.tileSize, gp.tileSize, null);

        }

    }

}
